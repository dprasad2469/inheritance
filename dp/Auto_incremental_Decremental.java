package dp;

public class Auto_incremental_Decremental {

	public static void main(String[] args) {
		int Dp = 10000;
		int Kp = 5000;
		
		
		Dp++; 		// Dp = Dp + 1
		
		Kp--;		// num2 = num2 - 1
		
		System.out.println("Output of Dp + 1 is "+ Dp);
		System.out.println("Output of Kp - 1 is "+ Kp);
	}

}
