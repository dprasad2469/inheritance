public class Arthimetic_operator {

	public static void main (String[] args) {
		byte a=12;
		short s=123;
		int i=2411;
		long J=987654321;
		float f=2.5f;
		double d=2.1487;
		System.out.println(a+s);
		System.out.println(J-i);
		System.out.println(f*d);
		System.out.println(i/s);
		System.out.println(J%i);
	}
}