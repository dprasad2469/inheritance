package Inheritence;

public class Child_class extends Parent_class {

	public static void main(String[] args) {
		
		crux();
		caliber();
		fz();
		yzf();
		getcolor();
	}
	
	public static void crux() {
		System.out.println("crux implemented");
	}
	
	public static void caliber() {
		System.out.println("caliber implemented");
	}
	public static void fz()	{
		System.out.println("fz method is implemented in child");
	}
	public static void yzf() {
		System.out.println("yzf method is implemented in child");
	}
	public static void getcolor() {
		System.out.println("blue");
	}
}
